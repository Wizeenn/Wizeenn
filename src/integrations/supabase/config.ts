type SupabaseConfig = {
  url: string;
  anonKey: string;
  serviceRoleKey?: string;
};

const missingEnv = (key: string) =>
  `Missing required environment variable: ${key}. Check your .env.local file.`;

export const getSupabaseConfig = (): SupabaseConfig => {
  const url = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const anonKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;

  if (!url) {
    throw new Error(missingEnv("NEXT_PUBLIC_SUPABASE_URL"));
  }

  if (!anonKey) {
    throw new Error(missingEnv("NEXT_PUBLIC_SUPABASE_ANON_KEY"));
  }

  return {
    url,
    anonKey,
    serviceRoleKey: process.env.SUPABASE_SERVICE_ROLE_KEY,
  };
};

